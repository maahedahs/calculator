    // Update value of the current input operand
    private void addToOperand(String digit) {
        if (isOperand) {
            operand = operand + digit;
        }
        else {
            operand = digit;
            isOperand = true;
        }
    }
    
    // Push current operand onto stack and clear the operand variable 
    private void setOperand(String operand) {
        postfixStack.push(operand);
        isOperand = false;
        this.operand = "";
    }
    
    // Add input operator to the postfix expression or stack based on operator precedence
    private void setOperation(String operator) {
        // Add current operand to stack
        if (isOperand)
            setOperand(operand);
        
        String topOperator;
        switch (operator) {
            case "(" :      // Always push ( to operatorStack
                operatorStack.push(operator);
                break;
            case ")" :      // Push elements to postfixStack until ( is encountered
                topOperator = operatorStack.pop();
                while (!topOperator.equals("(")) {
                    postfixStack.push(topOperator);
                    topOperator = operatorStack.pop();
                }
                break;
            case "+" : case "-" :
                // Push elements to postfixStack unless ( is encountered
                while (!operatorStack.isEmpty() && !operatorStack.peek().equals("(")) {
                    postfixStack.push(operatorStack.pop());
                }
                operatorStack.push(operator);   // Push + or - to operatorStack
                break;
            case "*" : case "÷" :
                // Push elements to postfixStack unless ( or an operator with a 
                // lower precedence is encountered
                while (!operatorStack.isEmpty() && !operatorStack.peek().equals("(") &&
                       !operatorStack.peek().equals("+") && !operatorStack.peek().equals("-")) {
                    postfixStack.push(operatorStack.pop());
                }
                operatorStack.push(operator);   // Push * or ÷ to operatorStack
                break;
        }
    }
    
    // Solve two-operand expression and return answer
    private double solveExpression(double operand1, double operand2, String operation) {
        switch (operation) {
            case "+" :
                return operand1 + operand2;
            case "-" :
                return operand1 - operand2;
            case "*" :
                return operand1 * operand2;
            case "÷" :
                return operand1 / operand2;
        }
        return 0.0;
    }
    
    // Clear all stacks and variables
    private void clear() {
        postfixStack.clear();
        operatorStack.clear();
        variableStack.clear();
        reversePostfix.clear();
        operand = "";
        expression = "";
        isOperand = false;
    }
    
