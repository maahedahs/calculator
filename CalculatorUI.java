    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Add operand to postfix expression if the last input character was part of an operand
        if (isOperand)
            setOperand(operand);
        
        // Push any remaining operators to postfix expression
        while (!operatorStack.isEmpty()) {
            postfixStack.push(operatorStack.pop());
        }
        
        // Fill reversePostfix with the reverse postfix expression, leftmost element at top
        while (!postfixStack.isEmpty()) {
            reversePostfix.push(postfixStack.pop());
        }
        
        // Solve postfix expression
        double operand1, operand2, answer;
        String nextCharacter;
        while (!reversePostfix.isEmpty()) {
            nextCharacter = reversePostfix.pop();
            switch (nextCharacter) {
                case "+" : case "-" : case "*" : case "÷" :
                    // solve once an operator is encountered
                    operand2 = variableStack.pop();
                    operand1 = variableStack.pop();
                    answer = solveExpression(operand1, operand2, nextCharacter);
                    variableStack.push(answer);
                    break;
                default :
                    // push element onto variableStack when an operand is encountered
                    variableStack.push(Double.parseDouble(nextCharacter));
                break;
            }
        }
        
        // Output final answer
        answer = variableStack.pop();
        jTextField2.setText(String.valueOf(answer));
        
        clear();    // Clear the contents of all stacks and variables
    }//GEN-LAST:event_jButton5ActionPerformed

